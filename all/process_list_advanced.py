def list_processes_advanced():
    """ĞŸĞ¾ĞºĞ°Ğ·Ñ‹Ğ²Ğ°ĞµÑ‚ Ñ€Ğ°ÑÑˆĞ¸Ñ€ĞµĞ½Ğ½Ñ‹Ğ¹ ÑĞ¿Ğ¸ÑĞ¾Ğº Ğ¿Ñ€Ğ¾Ñ†ĞµÑÑĞ¾Ğ²"""
    print("\nğŸ§© Ğ Ğ°ÑÑˆĞ¸Ñ€ĞµĞ½Ğ½Ñ‹Ğ¹ ÑĞ¿Ğ¸ÑĞ¾Ğº Ğ¿Ñ€Ğ¾Ñ†ĞµÑÑĞ¾Ğ²:")
    print("-" * 50)
    
    try:
        # ĞŸĞ¾Ğ»ÑƒÑ‡Ğ°ĞµĞ¼ ÑĞ¿Ğ¸ÑĞ¾Ğº Ğ¿Ñ€Ğ¾Ñ†ĞµÑÑĞ¾Ğ²
        processes = []
        for proc in psutil.process_iter(['pid', 'name', 'username', 'cpu_percent', 'memory_info', 'create_time']):
            try:
                processes.append(proc.info)
            except (psutil.NoSuchProcess, psutil.AccessDenied):
                pass
        
        # Ğ¡Ğ¾Ñ€Ñ‚Ğ¸Ñ€ÑƒĞµĞ¼ Ğ¿Ğ¾ Ğ¸ÑĞ¿Ğ¾Ğ»ÑŒĞ·Ğ¾Ğ²Ğ°Ğ½Ğ¸Ñ CPU
        processes.sort(key=lambda x: x['cpu_percent'] or 0, reverse=True)
        
        # ĞŸĞ¾ĞºĞ°Ğ·Ñ‹Ğ²Ğ°ĞµĞ¼ Ğ·Ğ°Ğ³Ğ¾Ğ»Ğ¾Ğ²Ğ¾Ğº Ñ‚Ğ°Ğ±Ğ»Ğ¸Ñ†Ñ‹
        print(f"{'PID':<8} {'Ğ˜Ğ¼Ñ':<25} {'CPU%':<8} {'ĞŸĞ°Ğ¼ÑÑ‚ÑŒ':<12} {'ĞŸĞ¾Ğ»ÑŒĞ·Ğ¾Ğ²Ğ°Ñ‚ĞµĞ»ÑŒ':<15}")
        print("-" * 75)
        
        # ĞŸĞ¾ĞºĞ°Ğ·Ñ‹Ğ²Ğ°ĞµĞ¼ Ğ¿ĞµÑ€Ğ²Ñ‹Ğµ 20 Ğ¿Ñ€Ğ¾Ñ†ĞµÑÑĞ¾Ğ²
        for i, proc in enumerate(processes[:20]):
            pid = proc['pid']
            name = proc['name'] or 'N/A'
            cpu = f"{proc['cpu_percent'] or 0:.1f}"
            
            # Ğ¤Ğ¾Ñ€Ğ¼Ğ°Ñ‚Ğ¸Ñ€ÑƒĞµĞ¼ Ğ¸ÑĞ¿Ğ¾Ğ»ÑŒĞ·Ğ¾Ğ²Ğ°Ğ½Ğ¸Ğµ Ğ¿Ğ°Ğ¼ÑÑ‚Ğ¸
            try:
                memory = proc['memory_info']
                memory_mb = f"{memory.rss / 1024 / 1024:.1f}MB" if memory else "N/A"
            except:
                memory_mb = "N/A"
            
            username = proc['username'] or 'N/A'
            
            # ĞĞ³Ñ€Ğ°Ğ½Ğ¸Ñ‡Ğ¸Ğ²Ğ°ĞµĞ¼ Ğ´Ğ»Ğ¸Ğ½Ñƒ Ğ¸Ğ¼ĞµĞ½Ğ¸
            if len(name) > 24:
                name = name[:21] + "..."
            
            print(f"{pid:<8} {name:<25} {cpu:<8} {memory_mb:<12} {username:<15}")
        
        print(f"\nĞ’ÑĞµĞ³Ğ¾ Ğ¿Ñ€Ğ¾Ñ†ĞµÑÑĞ¾Ğ²: {len(processes)}")
        
        # Ğ¡Ñ‚Ğ°Ñ‚Ğ¸ÑÑ‚Ğ¸ĞºĞ°
        total_cpu = sum(p['cpu_percent'] or 0 for p in processes)
        total_memory = sum((p['memory_info'].rss if p['memory_info'] else 0) for p in processes)
        total_memory_mb = total_memory / 1024 / 1024
        
        print(f"\nğŸ“Š Ğ¡Ñ‚Ğ°Ñ‚Ğ¸ÑÑ‚Ğ¸ĞºĞ° ÑĞ¸ÑÑ‚ĞµĞ¼Ñ‹:")
        print(f"   ĞĞ±Ñ‰ĞµĞµ Ğ¸ÑĞ¿Ğ¾Ğ»ÑŒĞ·Ğ¾Ğ²Ğ°Ğ½Ğ¸Ğµ CPU: {total_cpu:.1f}%")
        print(f"   ĞĞ±Ñ‰ĞµĞµ Ğ¸ÑĞ¿Ğ¾Ğ»ÑŒĞ·Ğ¾Ğ²Ğ°Ğ½Ğ¸Ğµ Ğ¿Ğ°Ğ¼ÑÑ‚Ğ¸: {total_memory_mb:.1f}MB")
        
    except Exception as e:
        print(f"âŒ ĞÑˆĞ¸Ğ±ĞºĞ° Ğ¿Ğ¾Ğ»ÑƒÑ‡ĞµĞ½Ğ¸Ñ ÑĞ¿Ğ¸ÑĞºĞ° Ğ¿Ñ€Ğ¾Ñ†ĞµÑÑĞ¾Ğ²: {e}")

