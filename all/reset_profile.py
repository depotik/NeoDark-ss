def get_neodark_config_dirs():
    """–ü–æ–ª—É—á–∞–µ—Ç –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ NeoDark"""
    config_dirs = []
    
    if os.name == 'nt':  # Windows
        config_dirs.append(os.path.join(os.environ.get('APPDATA', ''), 'NeoDark'))
        config_dirs.append(os.path.join(os.environ.get('LOCALAPPDATA', ''), 'NeoDark'))
    else:  # Unix-—Å–∏—Å—Ç–µ–º—ã
        config_dirs.append(os.path.join(os.path.expanduser('~'), '.neodark'))
        config_dirs.append(os.path.join(os.path.expanduser('~'), '.config', 'neodark'))
    
    return [d for d in config_dirs if os.path.exists(d)]

