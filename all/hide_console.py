'''
            
            script_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "start_hidden.sh")
            with open(script_path, 'w') as f:
                f.write(script_content)
            
            # –î–µ–ª–∞–µ–º —Å–∫—Ä–∏–ø—Ç –∏—Å–ø–æ–ª–Ω—è–µ–º—ã–º
            os.chmod(script_path, 0o755)
            
            print(f"   ‚úÖ –°–æ–∑–¥–∞–Ω —Å–∫—Ä–∏–ø—Ç: {script_path}")
            print("   üí° –°–¥–µ–ª–∞–π—Ç–µ –µ–≥–æ –∏—Å–ø–æ–ª–Ω—è–µ–º—ã–º: chmod +x start_hidden.sh")
            print("   üí° –ó–∞–ø—É—Å—Ç–∏—Ç–µ: ./start_hidden.sh")
            
        else:  # macOS
            # –°–æ–∑–¥–∞–µ–º shell-—Å–∫—Ä–∏–ø—Ç –¥–ª—è macOS
            script_content = f'''#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –∑–∞–ø—É—Å–∫–∞ NeoDark –±–µ–∑ –∫–æ–Ω—Å–æ–ª–∏
