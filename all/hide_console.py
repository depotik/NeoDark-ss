'''
            
            script_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "start_hidden.bat")
            with open(script_path, 'w') as f:
                f.write(script_content)
            
            print(f"   ‚úÖ –°–æ–∑–¥–∞–Ω —Å–∫—Ä–∏–ø—Ç: {script_path}")
            print("   üí° –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ —ç—Ç–æ—Ç —Ñ–∞–π–ª –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –±–µ–∑ –∫–æ–Ω—Å–æ–ª–∏")
            
        elif platform.system() == "Linux":
            # –°–æ–∑–¥–∞–µ–º shell-—Å–∫—Ä–∏–ø—Ç –¥–ª—è Linux
            script_content = f'''#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –∑–∞–ø—É—Å–∫–∞ NeoDark –±–µ–∑ –∫–æ–Ω—Å–æ–ª–∏
